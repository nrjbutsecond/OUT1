export function getTranslation(key: string, language: string = "en"): string {
  const lang = language as 'en' | 'vi'
  const translation = translations[lang]
  
  // Support nested keys like "nav.services"
  if (key.includes('.')) {
    return (translation as any)[key] || key
  }
  
  return (translation as any)[key] || key
}

export function useTranslation() {
  // Get language from localStorage or default to 'en'
  if (typeof window !== 'undefined') {
    const savedLanguage = localStorage.getItem("ton-language") || "en"
    return (key: string) => getTranslation(key, savedLanguage)
  }
  
  return (key: string) => getTranslation(key, "en")
}

// Hook for client components with reactive updates
export function useLanguage() {
  if (typeof window === 'undefined') {
    return 'en'
  }
  
  return localStorage.getItem("ton-language") || "en"
}
